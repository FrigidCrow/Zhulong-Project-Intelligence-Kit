import { spawnSync } from "node:child_process";
import {
  portableText,
  summarizeIssues,
  writeJsonReport,
  writeMarkdownReport,
} from "./quality-utils.mjs";
import { selectVerificationTasks } from "./verification-manifest.mjs";

const tier = process.argv[2] || "ci";
const tasks = selectVerificationTasks(tier);
const results = [];
const byId = new Map();
const startedAt = new Date().toISOString();

function failureTail(output, limit = 2400) {
  const clean = portableText(output || "").trim();
  return clean.length > limit ? clean.slice(-limit) : clean;
}

for (const task of tasks) {
  const blockedBy = task.dependsOn.filter((dependency) => byId.get(dependency)?.status !== "PASS");
  if (blockedBy.length) {
    const result = { id: task.id, script: task.script, status: "SKIP", durationMs: 0, blockedBy };
    results.push(result);
    byId.set(task.id, result);
    console.log(`\n[${task.id}] SKIP blocked by ${blockedBy.join(", ")}`);
    continue;
  }

  console.log(`\n[${task.id}] npm run ${task.script}`);
  const started = Date.now();
  const run = spawnSync("npm", ["run", task.script], {
    cwd: process.cwd(),
    encoding: "utf8",
    env: { ...process.env, ZL_VERIFICATION_TIER: tier },
    stdio: ["ignore", "pipe", "pipe"],
    timeout: 30 * 60 * 1000,
  });
  const output = `${run.stdout || ""}${run.stderr || ""}`;
  if (output.trim()) process.stdout.write(output.endsWith("\n") ? output : `${output}\n`);
  const result = {
    id: task.id,
    script: task.script,
    status: run.status === 0 ? "PASS" : "FAIL",
    exitCode: run.status,
    durationMs: Date.now() - started,
    failure: run.status === 0 ? "" : failureTail(output),
  };
  results.push(result);
  byId.set(task.id, result);
}

const failed = results.filter((item) => item.status === "FAIL");
const skipped = results.filter((item) => item.status === "SKIP");
const data = {
  schemaVersion: "zhulong-verification-index.v1",
  generated: new Date().toISOString(),
  startedAt,
  tier,
  status: failed.length ? "FAIL" : "PASS",
  summary: {
    total: results.length,
    passed: results.filter((item) => item.status === "PASS").length,
    failed: failed.length,
    skipped: skipped.length,
  },
  results,
};

const reportName = `verification-${tier}-index`;
writeJsonReport(`${reportName}.json`, data);
writeMarkdownReport(`${reportName}.md`, `Zhulong Verification Tier: ${tier}`, summarizeIssues(failed), [
  {
    title: "Summary",
    body: [
      `- Status: ${data.status}`,
      `- Passed: ${data.summary.passed}`,
      `- Failed: ${data.summary.failed}`,
      `- Skipped: ${data.summary.skipped}`,
    ],
  },
  {
    title: "Tasks",
    body: results.map((item) => `- ${item.status} \`${item.id}\` via \`npm run ${item.script}\` (${item.durationMs} ms)${item.blockedBy?.length ? `; blocked by ${item.blockedBy.join(", ")}` : ""}`),
  },
  {
    title: "Failures",
    body: failed.length ? failed.map((item) => `### ${item.id}\n\n\`\`\`text\n${item.failure || "No output captured."}\n\`\`\``) : ["No failures."],
  },
]);

if (tier === "release") {
  writeJsonReport("quality-closure-check.json", data);
  writeMarkdownReport("quality-closure-check.md", "Zhulong Quality Closure", summarizeIssues(failed), [
    { title: "Release Tier", body: [`- Status: ${data.status}`, `- Checks: ${results.length}`, `- Failed: ${failed.length}`, `- Skipped: ${skipped.length}`] },
    { title: "Source", body: ["- Generated by the single release verification manifest."] },
  ]);
}

console.log(`verification tier ${tier} ${data.status} passed=${data.summary.passed} failed=${failed.length} skipped=${skipped.length}`);
if (failed.length) process.exitCode = 1;
